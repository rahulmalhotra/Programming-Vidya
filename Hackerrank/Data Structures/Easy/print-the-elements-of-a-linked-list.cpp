/*
 * For your reference:
 *
 * SinglyLinkedListNode {
 *     int data;
 *     SinglyLinkedListNode* next;
 * };
 *
 */
void printLinkedList(SinglyLinkedListNode* head) {

    // * Loop while head pointer is not equal to NULL
    while(head!=NULL) {

        /*
        *   Display the data of the current node
        *   to which the head pointer is pointing
        */
        cout<<head->data<<endl;

        /*
        *   Move the head pointer to the next node
        *   of the linked list
        */
        head = head->next;
    }
}

