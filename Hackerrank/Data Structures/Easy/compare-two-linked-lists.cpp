/*
 * For your reference:
 *
 * SinglyLinkedListNode {
 *     int data;
 *     SinglyLinkedListNode* next;
 * };
 *
 */
bool compare_lists(SinglyLinkedListNode* head1, SinglyLinkedListNode* head2) {

    // * Looping while both the pointers are not NULL
    while(head1!=NULL && head2!=NULL) {

        /*
        *   If at any index,
        *   the data value of both the nodes
        *   are not equal, return false.
        *   As the linked lists are not equal
        */
        if(head1->data != head2->data) {
            return false;
        }

        // * Move head of first linked list to next node
        head1 = head1->next;

        // * Move head of second linked list to next node
        head2 = head2->next;
    }

    /*
    *   If any of the linked list has not
    *   reached the end, return false as
    *   the linked lists are not equal
    */
    if(head1!=NULL || head2!=NULL) {
        return false;
    }

    // * Return true as the linked lists are equal
    return true;
}

